# "Hello"와 "World"를 연결
combined = "Hello" + " " + "World"
print(combined)

# 대문자로 변환
uppercase = combined.upper()
print(uppercase)

# "World"만 슬라이싱
sliced = combined[6:]
print(sliced)

# 공백 기준 분리
text = "Python is fun"
split_text = text.split()
print(split_text)

# 짝수 인덱스 문자만 출력
text = "abcdef"
even_chars = text[0::2]
print(even_chars)

# "Hello" 3번 반복
repeated = "Hello" * 3
print(repeated)