# ["apple", "banana", "cherry"] 리스트에 "orange" 추가
fruits = ["apple", "banana", "cherry"]
fruits.append("orange")
print(fruits)

# [10, 20, 30] 리스트의 모든 요소의 합
numbers = [10, 20, 30]
total = sum(numbers)
print(f"합계: {total}")

# [1, 2, 3, 4, 5] 리스트의 요소들을 역순으로 출력
numbers = [1, 2, 3, 4, 5]
reversed_numbers = numbers[::-1]
print(reversed_numbers)

# [5, 2, 8, 1, 9] 리스트를 오름차순으로 정렬
numbers = [5, 2, 8, 1, 9]
numbers.sort()
print(numbers)